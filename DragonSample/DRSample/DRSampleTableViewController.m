#import "DRSampleTableViewController.h"
#define LIMIT 10

@interface DRSampleTableViewController ()
@property (strong, nonatomic) NSMutableArray *list;
@end

@implementation DRSampleTableViewController
@synthesize list = _list;
- (id)initWithStyle:(UITableViewStyle)style
{
    self = [super initWithStyle:style];
    if (self) {
        // Custom initialization
    }
    return self;
}

- (void)viewDidLoad
{
    [super viewDidLoad];
    self.tableView.delegate = self;
    self.tableView.dataSource = self;
    self.nextIndicatorDelegate = self;

    self.list = [[NSMutableArray alloc] init];
}
- (void)viewDidAppear:(BOOL)animated
{
    [self loadNext];
}

- (void)didReceiveMemoryWarning
{
    [super didReceiveMemoryWarning];
    // Dispose of any resources that can be recreated.
}
- (void)loadNext
{
    int offset = [self.list count]+1;
    [self startIndicatorWithActivityIndicatorStyle:UIActivityIndicatorViewStyleGray];
    for (int i=offset; i<offset + LIMIT; i++) {
        [self.list addObject:[NSString stringWithFormat:@"data%d", i]];
    }
    double delayInSeconds = 1.0;
    dispatch_time_t popTime = dispatch_time(DISPATCH_TIME_NOW, (int64_t)(delayInSeconds * NSEC_PER_SEC));
    dispatch_after(popTime, dispatch_get_main_queue(), ^(void){
        [self.tableView reloadData];
        [self stopIndicator];
    });
}
#pragma mark - Table view data source

- (NSInteger)numberOfSectionsInTableView:(UITableView *)tableView
{
    return 1;
}

- (NSInteger)tableView:(UITableView *)tableView numberOfRowsInSection:(NSInteger)section
{
    return [self.list count];
}

- (UITableViewCell *)tableView:(UITableView *)tableView cellForRowAtIndexPath:(NSIndexPath *)indexPath
{
    static NSString *CellIdentifier = @"Cell";
    UITableViewCell *cell = [self.tableView dequeueReusableCellWithIdentifier:CellIdentifier];
    if (cell==nil) {
        cell = [[UITableViewCell alloc] initWithStyle:UITableViewCellStyleDefault reuseIdentifier:CellIdentifier];
    }
    cell.textLabel.text = [self.list objectAtIndex:indexPath.row];
    return cell;
}

